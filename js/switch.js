function switchdi(IDa, IDb, IDc, IDd) {
  document.getElementById(IDa).style.display="none";
  document.getElementById(IDb).style.display="block";
  document.getElementById(IDc).style.height="23";
  document.getElementById(IDc).style.width="123";
  document.getElementById(IDd).style.height="20";
  document.getElementById(IDd).style.width="120";
}

